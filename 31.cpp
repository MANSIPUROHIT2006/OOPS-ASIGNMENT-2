// Implement a function that takes two integers and returns their product
#include <iostream>
using namespace std;
int main()
{
    int x, y;
    cout << "Enter a number" << endl;
    cin >> x;
    cout << "Enter another number" << endl;
    cin >> y;
    cout << "The product of " << x << " and " << y << " is : " << x * y << endl;
    return 0;
}
